/* fpga_design_config.h -- CI compile-only stub for wolfBoot
 *
 * This stub defines every LIBERO_SETTING_* symbol referenced by
 * hal/mpfs250.[ch] to 0.  It exists ONLY so that GitHub Actions can
 * compile-check the MPFS_DDR_INIT code path with the upstream
 * polarfire_mpfs250_m.config (which leaves LIBERO_FPGA_CONFIG_DIR
 * unset by default).
 *
 * Real boards must point LIBERO_FPGA_CONFIG_DIR at the actual Libero
 * / HSS-generated fpga_design_config directory (e.g.
 *   $HSS/build/boards/mpfs-video-kit/fpga_design_config
 * for the MPFS250T Video Kit).  The values defined there are
 * board-specific and required for DDR / PHY / training to work; this
 * stub will NOT produce a runnable bootloader.
 *
 * Regenerate with:
 *   grep -oh "LIBERO_SETTING_[A-Z_0-9]\+" \
 *     hal/mpfs250.c hal/mpfs250.h | sort -u | \
 *     awk '{print "#define " $0 " 0"}' > <this file>
 * Then re-add the header comment.
 */

